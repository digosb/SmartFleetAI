class Viagem:

    def __init__(
        self,
        nome,
        data,
        km_saida,
        hora_saida,
        km_chegada,
        hora_chegada,
        destino,
        carro,
        placa
    ):

        self.nome = nome
        self.data = data
        self.km_saida = km_saida
        self.hora_saida = hora_saida
        self.km_chegada = km_chegada
        self.hora_chegada = hora_chegada
        self.destino = destino
        self.carro = carro
        self.placa = placa